package com.publicissapient.weather.model;

/**
 * The Class Coord.
 *
 * @author Vinit Rathi
 * @version 1.0
 * @since 2022-09-05
 */
public class Coord {

    /** The lat. */
    private float lat;

    /** The lon. */
    private float lon;

    /**
     * Gets the lat.
     *
     * @return the lat
     */
    public float getLat() {
        return lat;
    }

    /**
     * Sets the lat.
     *
     * @param lat the lat to set
     */
    public void setLat(float lat) {
        this.lat = lat;
    }

    /**
     * Gets the lon.
     *
     * @return the lon
     */
    public float getLon() {
        return lon;
    }

    /**
     * Sets the lon.
     *
     * @param lon the lon to set
     */
    public void setLon(float lon) {
        this.lon = lon;
    }

}
